bool LoadTexture2DFromFile(RHI::Device& device, const char* path,
                           VkFormat format, RHI::Sampler::FilterMode filterMode,
                           RHI::Sampler::WrapMode wrapMode,
                           RHI::Texture& texture)
{
    FLY_ASSERT(path);

    Image image;
    if (!Fly::LoadImageFromFile(path, image))
    {
        return false;
    }

    if (!RHI::CreateTexture2D(device,
                              VK_IMAGE_USAGE_TRANSFER_DST_BIT |
                                  VK_IMAGE_USAGE_SAMPLED_BIT,
                              image.data, image.width, image.height, format,
                              filterMode, wrapMode, 0, texture))
    {
        return false;
    }
    FreeImage(image);
    return true;
}

u64 GetCompressedSize(u32 width, u32 height, VkFormat format)
{
    switch (format)
    {
        case VK_FORMAT_BC1_RGB_UNORM_BLOCK:
        case VK_FORMAT_BC1_RGB_SRGB_BLOCK:
        case VK_FORMAT_BC1_RGBA_UNORM_BLOCK:
        case VK_FORMAT_BC1_RGBA_SRGB_BLOCK:
        case VK_FORMAT_BC4_UNORM_BLOCK:
        case VK_FORMAT_BC4_SNORM_BLOCK:
        {
            return static_cast<u64>(Math::Ceil(width / 4.0f) *
                                    Math::Ceil(height / 4.0f) * 8);
            break;
        }
        case VK_FORMAT_BC3_UNORM_BLOCK:
        case VK_FORMAT_BC3_SRGB_BLOCK:
        case VK_FORMAT_BC5_UNORM_BLOCK:
        case VK_FORMAT_BC5_SNORM_BLOCK:
        {
            return static_cast<u64>(Math::Ceil(width / 4.0f) *
                                    Math::Ceil(height / 4.0f) * 16);
            break;
        }
        default:
        {
            return 0;
        }
    }
}

bool LoadCompressedTexture2D(RHI::Device& device, const char* path,
                             VkFormat format,
                             RHI::Sampler::FilterMode filterMode,
                             RHI::Sampler::WrapMode wrapMode,
                             RHI::Texture& texture)
{
    FLY_ASSERT(format == VK_FORMAT_BC1_RGB_UNORM_BLOCK ||
               format == VK_FORMAT_BC1_RGB_SRGB_BLOCK ||
               format == VK_FORMAT_BC1_RGBA_UNORM_BLOCK ||
               format == VK_FORMAT_BC1_RGBA_SRGB_BLOCK ||
               format == VK_FORMAT_BC3_UNORM_BLOCK ||
               format == VK_FORMAT_BC3_SRGB_BLOCK ||
               format == VK_FORMAT_BC4_UNORM_BLOCK ||
               format == VK_FORMAT_BC4_SNORM_BLOCK ||
               format == VK_FORMAT_BC5_UNORM_BLOCK ||
               format == VK_FORMAT_BC5_SNORM_BLOCK);

    Image image;
    if (!Fly::LoadImageFromFile(path, image))
    {
        return false;
    }

    if (!RHI::CreateTexture2D(device,
                              VK_IMAGE_USAGE_TRANSFER_DST_BIT |
                                  VK_IMAGE_USAGE_SAMPLED_BIT,
                              nullptr, image.width, image.height, format,
                              filterMode, wrapMode, image.mipCount, texture))
    {
        return false;
    }

    Arena& arena = GetScratchArena();
    ArenaMarker marker = ArenaGetMarker(arena);

    RHI::MipDesc* mips = FLY_PUSH_ARENA(arena, RHI::MipDesc, image.mipCount);
    for (u32 i = 0; i < image.mipCount; i++)
    {
        Mip mip;
        if (!GetMipLevel(image, i, mip))
        {
            return false;
        }
        mips[i].data = mip.data;
        mips[i].width = mip.width;
        mips[i].height = mip.height;
        mips[i].size = GetCompressedSize(mip.width, mip.height, format);
    }

    if (!CopyMipsToTexture(device, texture, mips, image.mipCount))
    {
        return false;
    }
    ArenaPopToMarker(arena, marker);

    FreeImage(image);
    return true;
}

static void RecordConvertFromEquirectangular(
    RHI::CommandBuffer& cmd, const RHI::RecordBufferInput* bufferInput,
    const RHI::RecordTextureInput* textureInput, void* pUserData)
{
    RHI::GraphicsPipeline* graphicsPipeline =
        static_cast<RHI::GraphicsPipeline*>(pUserData);

    RHI::Texture& equirectangular = *(textureInput->textures[0]);
    RHI::Texture& cubemap = *(textureInput->textures[1]);
    RHI::BindGraphicsPipeline(cmd, *graphicsPipeline);

    u32 indices[] = {equirectangular.bindlessHandle};
    RHI::PushConstants(cmd, indices, sizeof(indices));

    RHI::SetViewport(cmd, 0, 0, cubemap.width, cubemap.width, 0.0f, 1.0f);
    RHI::SetScissor(cmd, 0, 0, cubemap.width, cubemap.width);
    RHI::Draw(cmd, 6, 1, 0, 0);
}

static void RecordGenerateMipmaps(RHI::CommandBuffer& cmd,
                                  const RHI::RecordBufferInput* bufferInput,
                                  const RHI::RecordTextureInput* textureInput,
                                  void* pUserData)
{
    RHI::Texture& cubemap = *(textureInput->textures[1]);
    RHI::GenerateMipmaps(cmd, cubemap);
}

bool LoadCubemapEquirectangularFromFile(RHI::Device& device, const char* path,
                                        VkFormat format,
                                        RHI::Sampler::FilterMode filterMode,
                                        RHI::Texture& cubemap)
{
    FLY_ASSERT(path);

    Image image;
    if (!Fly::LoadImageFromFile(path, image))
    {
        return false;
    }
    FLY_ASSERT(image.width / 4 == image.height / 2);

    RHI::ShaderProgram shaderProgram{};
    if (!RHI::CreateShader(device,
                           reinterpret_cast<const char*>(screenQuadVertSpv),
                           SCREEN_QUAD_VERT_SPV_SIZE,
                           shaderProgram[RHI::Shader::Type::Vertex]))
    {
        return false;
    }
    if (!RHI::CreateShader(device,
                           reinterpret_cast<const char*>(screenQuadFragSpv),
                           SCREEN_QUAD_FRAG_SPV_SIZE,
                           shaderProgram[RHI::Shader::Type::Fragment]))
    {
        return false;
    }

    RHI::GraphicsPipelineFixedStateStage fixedState{};
    fixedState.pipelineRendering.colorAttachments[0] = VK_FORMAT_R8G8B8A8_SRGB;
    fixedState.pipelineRendering.colorAttachmentCount = 1;
    fixedState.colorBlendState.attachmentCount = 1;
    fixedState.pipelineRendering.viewMask = 0x3F;

    RHI::GraphicsPipeline graphicsPipeline;
    if (!RHI::CreateGraphicsPipeline(device, fixedState, shaderProgram,
                                     graphicsPipeline))
    {
        return false;
    }
    RHI::DestroyShader(device, shaderProgram[RHI::Shader::Type::Vertex]);
    RHI::DestroyShader(device, shaderProgram[RHI::Shader::Type::Fragment]);

    RHI::Texture texture2D;
    if (!RHI::CreateTexture2D(device,
                              VK_IMAGE_USAGE_TRANSFER_DST_BIT |
                                  VK_IMAGE_USAGE_SAMPLED_BIT,
                              image.data, image.width, image.height, format,
                              RHI::Sampler::FilterMode::Nearest,
                              RHI::Sampler::WrapMode::Clamp, 1, texture2D))
    {
        return false;
    }

    u32 size = image.width / 4;

    if (!RHI::CreateCubemap(device,
                            VK_IMAGE_USAGE_COLOR_ATTACHMENT_BIT |
                                VK_IMAGE_USAGE_SAMPLED_BIT |
                                VK_IMAGE_USAGE_TRANSFER_DST_BIT,
                            nullptr, size, format, filterMode, 0, cubemap))
    {
        return false;
    }
    FreeImage(image);

    VkRenderingAttachmentInfo colorAttachment =
        RHI::ColorAttachmentInfo(cubemap.arrayImageView);
    VkRenderingInfo renderingInfo = RHI::RenderingInfo(
        {{0, 0}, {size, size}}, &colorAttachment, 1, nullptr, nullptr, 6, 0x3F);

    RHI::RecordTextureInput textureInfo;
    RHI::Texture* textures[2];
    RHI::ImageLayoutAccess imageLayoutsAccesses[2];
    textureInfo.imageLayoutsAccesses = imageLayoutsAccesses;
    textureInfo.textures = textures;
    textureInfo.textureCount = 2;

    textures[0] = &texture2D;
    imageLayoutsAccesses[0].imageLayout =
        VK_IMAGE_LAYOUT_SHADER_READ_ONLY_OPTIMAL;
    imageLayoutsAccesses[0].accessMask = VK_ACCESS_2_SHADER_READ_BIT;

    textures[1] = &cubemap;
    imageLayoutsAccesses[1].imageLayout =
        VK_IMAGE_LAYOUT_COLOR_ATTACHMENT_OPTIMAL;
    imageLayoutsAccesses[1].accessMask = VK_ACCESS_2_COLOR_ATTACHMENT_WRITE_BIT;

    RHI::BeginTransfer(device);
    RHI::ExecuteGraphics(device, TransferCommandBuffer(device), renderingInfo,
                         RecordConvertFromEquirectangular, nullptr,
                         &textureInfo, &graphicsPipeline);
    RHI::ExecuteTransfer(device, TransferCommandBuffer(device),
                         RecordGenerateMipmaps, nullptr, &textureInfo);
    RHI::EndTransfer(device);

    RHI::DestroyTexture(device, texture2D);
    RHI::DestroyGraphicsPipeline(device, graphicsPipeline);
    return true;
}

bool LoadShaderFromSpv(RHI::Device& device, const char* path,
                       RHI::Shader& shader)
{
    FLY_ASSERT(path);

    Arena& scratch = GetScratchArena();
    ArenaMarker marker = ArenaGetMarker(scratch);

    String8 spvSource = Fly::LoadSpvFromFile(scratch, path);
    if (!spvSource)
    {
        return false;
    }
    if (!RHI::CreateShader(device, spvSource.Data(), spvSource.Size(), shader))
    {
        ArenaPopToMarker(scratch, marker);
        return false;
    }

    ArenaPopToMarker(scratch, marker);
    return true;
}

